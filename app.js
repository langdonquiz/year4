// Improved showScreen function to properly hide the main menu
function showScreen(screen) {
  // Hide all screens explicitly
  document.querySelectorAll(".screen").forEach(s => s.classList.add("hidden"));
  
  // Special case: Hide the main menu when showing the quiz screen
  if (screen === quizScreen) {
    mainMenu.classList.add("hidden");
  }

  // Show the selected screen
  screen.classList.remove("hidden");
}


// Improved loadMainMenu function for flexible term grouping
const termConfig = {
  "Autumn Term": 7,  // Number of quizzes in Autumn
  "Spring Term": 6,  // Number of quizzes in Spring
  "Summer Term": 5   // Number of quizzes in Summer
};

function loadMainMenu() {
  quizList.innerHTML = "";
  let quizIndex = 0;

  for (const [term, count] of Object.entries(termConfig)) {
    const termHeading = document.createElement("h2");
    termHeading.textContent = term;
    quizList.appendChild(termHeading);

    const quizArray = Object.keys(quizzes).slice(quizIndex, quizIndex + count);
    quizArray.forEach(title => {
      const btn = document.createElement("button");
      btn.textContent = title;
      btn.setAttribute("type", "button");
      btn.addEventListener("click", () => promptNameAndClass(title));
      quizList.appendChild(btn);
    });
    quizIndex += count;
  }
  showScreen(mainMenu); // Always start with the main menu
}

function promptNameAndClass(title) {
  currentTitle = title;
  // Clear any previous input
  nameInput.value = "";
  classInput.value = "";
  // Show the name and class entry screen after clicking a quiz
  showScreen(nameClassScreen);
}

startBtn.addEventListener("click", () => {
  studentName = nameInput.value.trim();
  studentClass = classInput.value.trim();
  startQuiz(currentTitle);
});

// Updated startQuiz function
function startQuiz(title) {
  currentQuiz = quizzes[title];
  currentTitle = title;
  currentQuestion = 0;
  score = 0;
  quizTitle.textContent = title;

  // Use showScreen to manage visibility correctly
  showScreen(quizScreen);
  showQuestion();
}


function showQuestion() {
  const question = currentQuiz[currentQuestion];
  questionText.textContent = question.q;
  answerButtons.innerHTML = "";
  feedback.classList.add("hidden");

  question.a.forEach((answer, index) => {
    const btn = document.createElement("button");
    btn.textContent = answer;
    btn.onclick = () => checkAnswer(index);
    answerButtons.appendChild(btn);
  });
}

function checkAnswer(selected) {
  const correctIndex = currentQuiz[currentQuestion].correct;
  feedback.classList.remove("hidden");
  if (selected === correctIndex) {
    feedback.textContent = "✅ Correct!";
    score++;
  } else {
    feedback.textContent = `❌ Wrong! The correct answer was: ${currentQuiz[currentQuestion].a[correctIndex]}`;
  }

  setTimeout(() => {
    currentQuestion++;
    if (currentQuestion < currentQuiz.length) {
      showQuestion();
    } else {
      showFinalScore();
    }
  }, 1500);
}

function showFinalScore() {
  let nameClassDisplay = "";
  if (studentName) nameClassDisplay += `Name: ${studentName} `;
  if (studentClass) nameClassDisplay += `Class: ${studentClass}`;
  finalScore.textContent = `${nameClassDisplay}\nYou scored ${score} out of ${currentQuiz.length}!`;

  // Send data to Google Sheets
  sendDataToGoogleSheets(studentName, studentClass, currentTitle, score);

  showScreen(scoreScreen);
}

